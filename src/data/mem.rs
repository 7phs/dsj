use std::io::{Cursor, Read, Result};
use std::slice;

struct Reader {
    data: Vec<u8>,
}

impl Reader {
    pub fn new(data: &[u8]) -> Reader {
        let data = Vec::from(data);

        Reader { data }
    }

    pub fn read_numbers(&mut self, numbers: &mut Vec<f64>) -> Result<()> {
        unsafe {
            let mut buffer =
                slice::from_raw_parts_mut(numbers.as_mut_ptr() as *mut u8, numbers.len() * 8);
            Cursor::new(&self.data).read_exact(&mut buffer)
        }
    }
}

pub fn read_mem() {
    let src_buffer: [u8; 9680] = data();
    let mut numbers = vec![0.0f64; 1100];
    let mut reader = Reader::new(&src_buffer);

    if let Err(err) = reader.read_numbers(&mut numbers) {
        println!("failed to read numbers with {:?}", err);
    } else {
        println!("read numbers {:?}", &numbers);
    }
}

